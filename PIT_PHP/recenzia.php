		<h2> Detroit: Become Human </h2> <p>
		
		<B> Vývoj: </B> Quantic Dream <br> <B> Platformy: </B> PS4, PC <br> <B> Žáner: </B> Akčná adventúra, interaktívny príbeh <br> <B> Dátum vydania: </B> 25. máj 2018 <p>
		
		25. mája 2018 sme sa dočkali celkom dlho očakávanej hry Detroit: Become Human exkluzívne pre PS4 a neskôr 12. decembra 2019 aj na PC. 
		Ide o hru na štýl interaktívneho filmu, kde hlavnou motiváciou nie je ani tak samotná hratelnosť, 
		ale skôr filmový/cinematický zážitok, ktorého dej môžete ovplyvňovať rozhodnutiami.
		Štúdio Quantic Dream sľubovalo naozaj veľa. Všetky trailery a propagačné materiály poukazovali, na naozaj veľké množstvo rozhodnutí 
		a ako si hráči budú môcť vyskladať vlastný príbeh. Mnoho ľudí sa tešilo, iní boli skeptickí. Poznáme predsa podobné hry, ktoré tiež sľubovali ovplyvnenie deja rozhodnutiami.
		Ja som patril k tej prvej skupine, Quantic dream totiž už v roku 2010 vydali hru Heavy Rain, a o 3 roky neskôr o niečo slabšie no príbehovo veľmi
		zaujímavé Beyond Two Souls. Tieto 2 hry som si veľmi obľúbil a bolo mi jasné, že oni už nejaké skúsenosti s týmto štýlom hier majú. <p>
		
		Dostávame sa do "kože" trojice hlavných hrdinov (Connor, Kara a Markus) v meste Detroit v roku 2038. Aby som nezabudol, títo traja sú androidi, no od ľudí ich spoznáte 
		iba podľa oblečenia a LED diódy na pravom spánku. Androidi tu majú za úlohu slúžiť ľuďom a pomáhať im. Varia, starajú sa o deti, starajú sa o chorých a starých... 
		no na druhú stranu sú tak strašne "dokonalí", že v USA spôsobili mohutný nárast nezamestnanosti tým, že si jednoducho zamestnávatelia radšej vybrali androida,
		ktorý sa neunaví, nezmýli, je lacnejší, nie sú za nich zodpovednosti ako za ľudské životy a podobne. Aby toho nebolo málo, v Detroite sa začnú objavovať prípady
		takzvaných deviantov. Devianti sú androidi, v ktorých nejaký emocionálny šok prebudil vedomie. Strach, zlosť, vôľa žiť. Všetko zrazu vnímali ako ľudia.
		No keď sa pozreli na svoj vlastný druh, videli iba otrokov a spotrebný tovar. Revolúcia sa mohla začať. <p>
		
		Ako prvé ma na tejto hre zaujal vizuál. Tá hra proste vyzerá fantasticky. A to ani nehovorím o zvuku. Hudba a zvukové efekty sú v tejto hre geniálne.
		Všetky 3 postavy, za ktoré ste mohli hrať boli svojím spôsobom sympatické. A keďže každá z postáv mala inú motiváciu, aj tempo hry sa stále striedalo.
		Najviac som si ale užíval hru za detektívneho androida Connora, ktorý dostal prípad narastajúceho počtu deviantov v Detroite. Pracovať na ňom mal
		s poručíkom Hankom Andersonom. Títo dvaja boli mojími najobľúbenejšími postavami z celej hry. 
		Karu a Markusa som mal tak na rovnako v obľube. Páčila sa mi aj osobnejšia linka Kary, aj Markusov boj za slobodu androidov.
		Za zmienku stoja aj časopisy, ktoré viac rozvíjajú svet v ktorom sa dej odohráva. Nezvyknem to robiť, no tu som sa pravidelne zastavoval, aby som
		si nejaký ten článok o tomto fiktívnom svete prečítal. Keď sa však človek zamyslí, občas je to až desivé ako veľmi niektoré články dávali zmysel
		a ako veľmi uveritelný svet to je. 
		Najlepší bol ale pocit, že počas hry naozaj ovplyvňujete vývoj deja vašími rozhodnutiami. A poviem vám, nie je to len pocit.
		Tá hra má okolo 40 rôznych koncov. Ešte aj malé rozhodnutia pôsobia ako niečo dôležité, pretože sčítavaním malých rozhodnutí sa vám odomykajú väčšie rozhodnutia.
		Keby vám aj chcem vyspojliť, ktoré postavy zomrú, nepodarilo by sa mi to, pretože smrte hlavných aj vedlajších postáv závisia od vaších rozhodnutí.
		Po dohraní Detroitu som vedel, že nepôjde o len tak ľahko zabudnutelnú hru.
		Prvé odohranie mi trvalo tak 9-10 hodín, čo nie je veľa, ale treba brať ohľad aj na to, 
		že táto hra je takmer film, čiže si hraciu dobu nepredĺžite prechodom z bodu A do bodu B ako pri RPG hrách.
		Namiesto toho si môžete hrací čas natiahnúť skúšaním iných rozhodnutí. <p>
		
		Samozrejme, že hru s viac koncami som chcel skúšať znova a znova a preskúmavať čo všetko sa môže stať inak. 
		Na druhé zahranie som si ale začal všímať nedostatky tejto hry. Prvý krát ma mohol trochu zaslepiť hype.
		Trochu nemotorné ovládanie, napríklad ak som chcel interaktizovať s nejakým predmetom, musel som vykonať pohyb s páčkou,
		ako to odo mňa tá hra vyžadovala. Na tom by nebolo nič zlé, ale bola to tá istá páčka, s ktorou som sa rozhliadal a trochu ma štvalo,
		keď namiesto chcenej interakcie som sa začal rozhliadať. To isté platí naopak keď som sa chcel rozhliadnúť a namiesto toho vzal niečo do ruky.
		Pamätám si aj zopár trochu odfláknutých modelov, ako napríklad hranatejšie auto, ktoré nezapadalo do zvyšku sveta. Ale to je veľmi zanedbateľné. 
		Niekto by mohol Detroit prirovnať k walking simulatoru. Bežať môžete, len keď to hra od vás vyžaduje.
		No toto mi ani tak nevadilo, pretože hustota veľmi kvalitného príbehu to pre mňa dokázala zamaskovať.
		Ďalším problémom boli Quick Time Eventy. Normálne mi nevadia, ak nie sú zbytočne rýchle a neprehľadné, dokážu hráča zapojiť do cinematickej časti.
		Tu to ale bola bombardácia Quick Time Eventami.
		Takto som si občas nevedel ani poriadne užiť akčnú sekvenciu, pretože som sa viac sústredil na to aké tlačítko budem musieť stlačiť.
		Občas aj časované rozhodnutia pôsobili ako Quick Time Event. Určite túto hru neodporúčam začiatočníkom.
		Ak však máte skúsenosti s hrami tohto štýlu a bavia vás, nemôžem inak než len doporučiť.
		Detroit nie je perfektná hra, má svoje muchy, no momentálne je to to najlepšie, čo nám kategória Interactive Story ponúka.
		Je jasné, že keď si túto hru zahráte druhý krát, tretí krát, nebudete hrať úplne inú hru, ako si možno niektorí predstavovali.
		Treba však povedať, že ešte sme tu nemali žiadnu inú hru, kde by ste mohli natoľko ohýbať dej ako práve v Detroite. <p>
		
		
		
